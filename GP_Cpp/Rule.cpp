#include "Rule.h"


/*********************************************************************
* Comment
*********************************************************************/
Rule::Rule(string name, vector<string> elements)
{
   ruleName = name;
   members = elements;
}

/*********************************************************************
* Comment
*********************************************************************/
Rule::~Rule() {}


/*********************************************************************
* Comment
*********************************************************************/
int Rule::numSymbols()
{
   return members.size();
}

/*********************************************************************
* Comment
*********************************************************************/
string Rule::getRuleName()
{
   return ruleName;
}

string Rule::getElement(int i) {
   return members[i];
}
